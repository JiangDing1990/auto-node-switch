/**
 * 获取带颜色的横幅
 */
export function getColoredBanner(
	type: 'full' | 'compact' | 'mini' | 'stylish' | 'exit' = 'compact',
): string {
	switch (type) {
		case 'full': {
			return ASCII_BANNER;
		}

		case 'compact': {
			return COMPACT_BANNER;
		}

		case 'mini': {
			return MINI_BANNER;
		}

		case 'stylish': {
			return STYLISH_BANNER;
		}

		case 'exit': {
			return EXIT_BANNER;
		}

		default: {
			return COMPACT_BANNER;
		}
	}
}

